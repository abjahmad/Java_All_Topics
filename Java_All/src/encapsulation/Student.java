package encapsulation;

/*
 * Encapsulation in Java
 * 
 * Encapsulation in java  is a process of wrapping code and data into a single unit 
 * 
 * we can create fully encapsulated  class in java by making all the data private 
 * 
 * we can use setter and getter method set and get data 
 * 
 * 
*/


public class Student {

	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
